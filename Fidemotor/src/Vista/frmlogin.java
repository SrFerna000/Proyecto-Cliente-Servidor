    public void validar(){
        String identificacion= txtIdentificacion.getText();
        String contraseña= txtContraseña.getText();
        if(txtIdentificacion.getText().equals("")||txtContraseña.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar datos en las cajas de texto");
            txtIdentificacion.requestFocus();
        }else{
            u=udao.ValidarUsuario(contraseña, identificacion);
            if(u.getIdentificacion() !=null && u.getContrasena()!=null){
                Menu m= new Menu();
                m.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Debe ingresar datos validos");
                txtIdentificacion.requestFocus();
            }
        }
    }
   
